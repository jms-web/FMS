ALTER VIEW [dbo].[VWM016_SYONIN_TANTO]
AS
SELECT
 M016.SYONIN_HOKOKUSYO_ID
,M013.SYONIN_HOKOKUSYO_R_NAME
,M016.SYONIN_JUN
,ISNULL(M014.SYONIN_NAIYO,'') AS SYONIN_NAIYO
,M016.SYAIN_ID
,ISNULL((SELECT SYAIN_NO FROM M004_SYAIN AS M WHERE(M016.SYAIN_ID = M.SYAIN_ID)),'') AS SYAIN_NO
,ISNULL((SELECT SIMEI FROM M004_SYAIN AS M WHERE(M016.SYAIN_ID = M.SYAIN_ID)),'') AS SYAIN_NAME
,ISNULL(VWM005.BUMON_KB,'') AS BUMON_KB
,ISNULL(VWM005.BUMON_KB_DISP,'') AS BUMON_KB_DISP
,M016.ADD_YMDHNS
,M016.ADD_SYAIN_ID
,ISNULL((SELECT SIMEI FROM M004_SYAIN AS M WHERE(M014.UPD_SYAIN_ID = M.SYAIN_ID)),'') AS ADD_SYAIN_NAME
FROM
  M016_SYONIN_TANTO AS M016
	LEFT JOIN M013_SYONIN_HOKOKU AS M013 ON (M016.SYONIN_HOKOKUSYO_ID = M013.SYONIN_HOKOKUSYO_ID)
	LEFT JOIN M014_SYONIN_ROUT AS M014 ON (M016.SYONIN_HOKOKUSYO_ID = M014.SYONIN_HOKOKUSYO_ID AND M016.SYONIN_JUN = M014.SYONIN_JUN) 
	LEFT JOIN VWM005_SYOZOKU_BUSYO AS VWM005 ON (M016.SYAIN_ID = VWM005.SYAIN_ID) 